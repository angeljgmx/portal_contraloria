 function PageTitle2($path, $page_title){
        
        // inclusion de archivos nesesarios
        require_once $path.'includes/funciones.php';
        
        // Obtencion del arreglo con la fecha y hora actual
        $fecha = time();
        
        // Inclusion de la clase para el breadcrumbs
        include $path.'includes/class.breadcrumb.inc.php';
        
        // Inicializacion de la clase
        $breadcrumb = new breadcrumb;
        
        // Removemos el directorio para que no se muestre
        $breadcrumb->removeDirs = array ("private", "public");
        
        // No mostramos la extencion del archivo
        $breadcrumb->hideFileExt = TRUE;
        
        // Primera letra en mayusculas para los elementos del breadcrumbs
        $breadcrumb->dirformat='ucfirst';
        
        // Cambiamos "_" por "" en los nombres de los elementos
        $breadcrumb->_toSpace = TRUE;                   
        
        echo "<section class=\"top_title triggerAnimation animated\" data-animate=\"fadeIn\"\" > \n"
            ."<div class=\"container\"> \n"
            ."<div class=\"row\"> \n"
            ."<div class=\"grid_12\"> \n"
            ."<div class=\"pt-title triggerAnimation animated\" data-animate=\"fadeInLeft\"> \n"
            ."<h2>".$page_title."<span>".$breadcrumb->show_breadcrumb()."</span></h2> \n"
            ."<div id=\"top_title_date\"> \n"
            // fecha
            .FechaActual($fecha) 
            ."</div> \n"
            ."</nav> \n"
            ."</div> \n"
            ."</div><!-- .grid_6 end --> \n"
            ."</div><!-- .row end --> \n"

            ."</div><!-- .container end --> \n"
            ."</section><!-- #page-title end --> \n";
    }    
    
